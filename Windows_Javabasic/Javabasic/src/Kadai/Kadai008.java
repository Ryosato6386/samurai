package Kadai;

public class Kadai008 {
    public static void main(String[] args) {
    	
    	int money = 3;
    	
		switch(money) {
	    case 1  -> System.out.println("10代の料金は1000円");
	    case 2  -> System.out.println("20代の料金は2000円");
	    case 3  -> System.out.println("30代の料金は3000円");
	    case 4  -> System.out.println("30代の料金は4000円");
	    case 5  -> System.out.println("30代の料金は5000円");
	    default -> System.out.println("500円");
    	
    	
    }

}
}

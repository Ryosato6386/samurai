let userName;
// 値の代入
userName = "ハム太郎";

// 変数の宣言・値の代入
let userNumber = 55;
// コンソールへの出力
console.log(userName);
console.log(userNumber);

// 値の再代入
userName = 'ハム子';
userNumber = 88;
// コンソールへの出力
console.log(userName);
console.log(userNumber);


